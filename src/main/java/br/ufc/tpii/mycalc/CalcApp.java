package br.ufc.tpii.mycalc;

public class CalcApp {

	public double add(double a, double b) {
		return a + b;
	}

	public double minus(double a, double b) {
		return a - b;
	}

	public double plus(double a, double b) {
		return a * b;
	}

	public double div(double a, double b) {
		return a / b;
	}
}
